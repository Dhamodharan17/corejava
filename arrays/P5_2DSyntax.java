package arrays;

public class P5_2DSyntax {

    public static void main(String[] args) {

        int[][] a ={{10,20,30},{40,50}};

        System.out.println(a[0]);//[I@3f0ee7cb
        System.out.println(a[1]);//[I@3f0ee7cb

        System.out.println(a[0][0]);//10
        System.out.println(a[1][0]);//40
    }
}
