package flowcontrol;

public class iterativestamts {

    public static void main(String[] args) {

        //Iterative Statements - used to perform multiple operation on few line
        System.out.println("Hello");
        System.out.println("Hello");
        System.out.println("Hello");
        System.out.println("Hello");
        System.out.println("Hello");

        System.out.println("Start of For Loop");
        for (int i =1;i<6;i++){
            System.out.println("Hello");
        }



    }
}
